namespace Finna\AjaxHandler;

use Laminas\Mvc\Controller\Plugin\Params;

/**
 * "Get Item Status" AJAX handler
 *
 * This is responsible for printing the holdings information for a
 * collection of records in JSON format.
 *
 * @category VuFind
 * @package  AJAX
 * @author   Ere Maijala <ere.maijala@helsinki.fi>
 * @license  http://opensource.org/licenses/gpl-2.0.php GNU General Public License
 * @link     https://vufind.org/wiki/development Wiki
 */
class GetItemStatuses extends \VuFind\AjaxHandler\GetItemStatuses
{
    /**
     * Handle a request.
     *
     * @param Params $params Parameter helper from controller
     *
     * @return array [response data, HTTP status code]
     */
    public function handleRequest(Params $params)
    {
        $result = parent::handleRequest($params);
        // Add full_status to missing records
        if (!empty($result[0]['statuses'])) {
            foreach ($result[0]['statuses'] as &$status) {
                if (!empty($status['missing_data'])) {
                    $status['full_status']
                        = $this->translate('No Holdings Available');
                }
            }
            // unset reference
            unset($status);
        }

        return $result;
    }
}
